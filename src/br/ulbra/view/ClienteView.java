    public void listarTabela() {

        DefaultTableModel model = (DefaultTableModel) tblCliente.getModel();
        model.setRowCount(0);

        for (br.ulbra.model.Cliente c : controller.listar()) {
            model.addRow(new Object[]{
                c.getId(),
                c.getNomeclien(),
                c.getDatanasc(),
                c.getCpf()
            });
        }
    }

