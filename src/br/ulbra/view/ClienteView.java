    br.ulbra.controller.ClienteController controller = new br.ulbra.controller.ClienteController();
    private void btnSalvarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarCActionPerformed
        String mensagem = controller.cadastrar(
                txtNomeClie.getText(),
                txtCPF.getText(),
                txtDataNasc.getText()
        );
        javax.swing.JOptionPane.showMessageDialog(null, mensagem);
        listarTabela();
    }//GEN-LAST:event_btnSalvarCActionPerformed

    private void btnEditarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarCActionPerformed
        Long id = Long.parseLong(txtId.getText());
        String msg = controller.atualizar(
                id,
                txtNomeClie.getText(),
                txtCPF.getText(),
                txtDataNasc.getText()
        );
        JOptionPane.showMessageDialog(null, msg);
        listarTabela();
    }//GEN-LAST:event_btnEditarCActionPerformed

    private void btnExcluirCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirCActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null, " Deseja excluir o Cliente"
                + txtNome.getText() + " ?", "Exclusão ", JOptionPane.YES_NO_OPTION);
        Long id = Long.parseLong(txtId.getText());
        if (confirm == JOptionPane.YES_OPTION) {
            String msg = controller.deletar(id);
            JOptionPane.showMessageDialog(null, msg);
            listarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Operação cancelada!");
        }
    }//GEN-LAST:event_btnExcluirCActionPerformed

    private void btnLimparCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparCActionPerformed
        txtId.setText(null);
        txtNomeClie.setText(null);
        txtDataNasc.setText(null);
        txtCPF.setText(null);
        txtId.setFocusable(true);
    }//GEN-LAST:event_btnLimparCActionPerformed

    private void tblClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClienteMouseClicked
        int linha = tblCliente.getSelectedRow();
        txtId.setText(tblCliente.getValueAt(linha, 0).toString());
        txtNomeClie.setText(tblCliente.getValueAt(linha, 1).toString());
        txtDataNasc.setText(tblCliente.getValueAt(linha, 2).toString());
        txtCPF.setText(tblCliente.getValueAt(linha, 3).toString());
    }//GEN-LAST:event_tblClienteMouseClicked

